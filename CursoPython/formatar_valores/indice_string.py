# pode-se acessar o índice de uma string
# para isso utilizar chaves
# ex:
#indice 01234
nome = "Pedro"

#para acessar o 'o' de Pedro por exemplo, colocar o indice entre chaves
#no caso o 'o' seria o 4° no índice, entao colocar entre chaves [4]

print(nome[4])

#irá imprimir 'o'

#pode ser usado também o índice de forma negativa
#por exemplo, eu quero remover o 'o' de Pedro
#basta imprimir com o sinal de -
#neste caso, o indice é lido de outra forma
#indice -54321
#nome = "Pedro"

print(nome[:-1])

# irá imprimir 'Pedr'