# para dividir uma string, utilizar a função split.
# no caso será divida em uma lista (vetor)

a = "Estudar Python é bom demais!"

v = a.split(" ")
# ao informar (" ") estou dizendo que a string deve ser separada
# a cada espaço vazio " ". A lista vai apresentar cada palavra
# como um elemento

print(v)

# irá imprimir ['Estudar', 'Python', 'é', 'bom', 'demais!']