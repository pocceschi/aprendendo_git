# ao escrever um código, pode-se utilizar a função pass para
# alocar uma função que ainda não está pronta
# ao colocar pass o código irá "saltar" a função até que seja corrigida.
# como se fosse um substituto temporário
# ao invés de pass pode-se usar três pontos também (...)

# ex:

valor = False

if valor:
    pass
else:
    print("Incorreto")